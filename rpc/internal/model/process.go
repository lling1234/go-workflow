package model

type AutoGenerated struct {
	Process Node `json:"process"`
}
type FormPerms struct {
	ID       string `json:"id"`
	Perm     string `json:"perm"`
	Title    string `json:"title"`
	Required bool   `json:"required"`
}

type Leader struct {
	Level int `json:"level"`
}
type Nobody struct {
	Handler      string        `json:"handler"`
	AssignedUser []interface{} `json:"assignedUser"`
}
type Refuse struct {
	Type   string `json:"type"`
	Target string `json:"target"`
}
type LeaderTop struct {
	EndLevel     int    `json:"endLevel"`
	EndCondition string `json:"endCondition"`
}
type Notify struct {
	Hour int  `json:"hour"`
	Once bool `json:"once"`
}
type Handler struct {
	Type   string `json:"type"`
	Notify Notify `json:"notify"`
}
type Timeout struct {
	Unit  string `json:"unit"`
	Value int    `json:"value"`
}
type TimeLimit struct {
	Handler Handler `json:"handler"`
	Timeout Timeout `json:"timeout"`
}
type SelfSelect struct {
	Multiple bool `json:"multiple"`
}
type AssignedUser struct {
	ID       int         `json:"id"`
	Sex      interface{} `json:"sex"`
	Name     string      `json:"name"`
	Type     string      `json:"type"`
	Selected bool        `json:"selected"`
}
type Props struct {
	Mode         string         `json:"mode"`
	Role         []interface{}  `json:"role"`
	Sign         bool           `json:"sign"`
	Leader       Leader         `json:"leader"`
	Nobody       Nobody         `json:"nobody"`
	Refuse       Refuse         `json:"refuse"`
	FormUser     string         `json:"formUser"`
	FormPerms    []FormPerms    `json:"formPerms"`
	LeaderTop    LeaderTop      `json:"leaderTop"`
	TimeLimit    TimeLimit      `json:"timeLimit"`
	SelfSelect   SelfSelect     `json:"selfSelect"`
	AssignedType string         `json:"assignedType"`
	AssignedUser []AssignedUser `json:"assignedUser"`
}

// ---------------------------
// 7个字段
type Node struct {
	ID       string      `json:"id"`
	Desc     string      `json:"desc"`
	Name     string      `json:"name"`
	Type     string      `json:"type"`
	Props    Propss      `json:"props"`
	Children Children    `json:"children"`
	ParentID interface{} `json:"parentId"`
}
type Propss struct {
	FormPerms    []FormPerms   `json:"formPerms"`
	AssignedUser []interface{} `json:"assignedUser"`
}

// 6个字段
type Children struct {
	ID       string        `json:"id"`
	Name     string        `json:"name"`
	Type     string        `json:"type"`
	Props    Props         `json:"props"`
	Children *Children     `json:"children"`
	ParentID string        `json:"parentId"`
	Branchs  []interface{} `json:"branchs"`
}
